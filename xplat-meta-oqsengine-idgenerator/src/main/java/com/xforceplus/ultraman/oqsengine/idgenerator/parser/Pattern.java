package com.xforceplus.ultraman.oqsengine.idgenerator.parser;


/**
 * Pattern constant.
 *
 * @author leo
 */
public interface Pattern {
    /**
     * year {yyyy}.
     *
     */
    String YEAR = "{yyyy}";

    /**
     * month  {MM}.
     *
     */
    String MONTH = "{MM}";

    /**
     * day {dd}.
     *
     */
    String DAY = "{dd}";

    /**
     * Hour {HH}.
     */
    String HOUR = "{HH}";

    /**
     * number.
     *
     */
    String NUMBER = "{0}";

}
